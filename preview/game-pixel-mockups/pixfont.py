def measure(text):
    w = 0
    for ch in text:
        top, rows = G.get(ch, G['?'])
        gw = max(len(r) for r in rows)
        w += gw + 1
    return max(0, w - 1)

def _pixels(x, y, text):
    cx = x
    out = []
    for ch in text:
        top, rows = G.get(ch, G['?'])
        gw = max(len(r) for r in rows)
        for ry, row in enumerate(rows):
            for rx, c in enumerate(row):
                if c == '#':
                    out.append((cx + rx, y + top + ry))
        cx += gw + 1
    return out, cx

def draw_text(img, x, y, text, color, shadow=None):
    """img: PIL Image. y = top of the 9-row cell. Returns end x."""
    pts, endx = _pixels(x, y, text)
    iw, ih = img.size
    if shadow is not None:
        for (px_, py_) in pts:
            if 0 <= px_ + 1 < iw and 0 <= py_ + 1 < ih:
                img.putpixel((px_ + 1, py_ + 1), shadow)
    for (px_, py_) in pts:
        if 0 <= px_ < iw and 0 <= py_ < ih:
            img.putpixel((px_, py_), color)
    return endx
