/**
 * Generate `src/data/frequency.ts` — the shipped per-item word-frequency map
 * behind the Häufigkeit facet/badge and the Fortschritt composition chart
 * (Bibliothek categorization plan PR 3, audit 2026-07-09).
 *
 *   pnpm build:frequency   # regenerate src/data/frequency.ts
 *
 * Reads the already-vendored `scripts/vendor/german-frequency-subset.json`
 * (wordfreq Zipf facts, Apache-2.0 / MIT; regenerate that subset first with
 * `pnpm build:frequency-subset` after adding vocab/collocations) and bins each
 * content item's HEADWORD Zipf into the existing `Frequency` enum:
 *
 *   core        Zipf >= 4.5   "Kernwortschatz" — everyday high-frequency German
 *   common      Zipf >= 3.5   "häufig"
 *   specialized Zipf >= 1.5   "Fachsprache" — attested but rare in everyday text
 *
 * THE CORRECTNESS RULE (do not weaken it): below 1.5 — including Zipf 0 — the
 * item gets NO bin and rolls up as untagged. German compounds are rare BY
 * CONSTRUCTION (Aufenthaltserlaubnis scores 0), so absence of corpus evidence
 * must never be labelled "Fachsprache". Only positive low-frequency evidence is.
 *
 * The output is generated — do not hand-edit (same contract as verification.ts).
 */
import path from "node:path";
import { fileURLToPath } from "node:url";
import { readFile, writeFile } from "node:fs/promises";
import { createServer } from "vite";
import { headToken } from "./build-frequency-subset.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SUBSET = path.join(root, "scripts", "vendor", "german-frequency-subset.json");
const OUT = path.join(root, "src", "data", "frequency.ts");

const BIN_MIN = [
  ["core", 4.5],
  ["common", 3.5],
  ["specialized", 1.5],
];

function binFor(zipf) {
  if (typeof zipf !== "number" || !Number.isFinite(zipf)) return null;
  for (const [bin, min] of BIN_MIN) if (zipf >= min) return bin;
  return null; // < 1.5 incl. 0/OOV: no claim (compound rule)
}

async function loadBanks() {
  const server = await createServer({
    root,
    configFile: path.join(root, "vite.config.ts"),
    logLevel: "error",
    optimizeDeps: { noDiscovery: true, entries: [] },
    server: { middlewareMode: true, hmr: false },
    appType: "custom",
  });
  try {
    const vocabulary = (await server.ssrLoadModule("/src/data/vocabulary.ts")).vocabulary;
    const collocations = (await server.ssrLoadModule("/src/data/collocations.ts")).collocations;
    return { vocabulary, collocations };
  } finally {
    await server.close();
  }
}

async function main() {
  const { freq } = JSON.parse(await readFile(SUBSET, "utf8"));
  const { vocabulary, collocations } = await loadBanks();

  const rows = [];
  const stats = { core: 0, common: 0, specialized: 0, unbinned: 0 };
  const add = (id, headword) => {
    const token = headToken(headword);
    const zipf = token ? freq[token] : undefined;
    const bin = binFor(zipf);
    if (bin) {
      rows.push(`  "${id}": { bin: "${bin}", zipf: ${zipf} },`);
      stats[bin]++;
    } else {
      stats.unbinned++;
    }
  };
  for (const v of vocabulary) add(v.id, v.de);
  for (const c of collocations) add(c.id, c.noun ?? c.full);

  const total = vocabulary.length + collocations.length;
  const binned = total - stats.unbinned;
  const today = new Date().toISOString().slice(0, 10);

  const file = `// AUTO-GENERATED by scripts/build-frequency.mjs — do not edit by hand.
// Per-item word frequency (Häufigkeit), binned from wordfreq Zipf facts
// (wordfreq on PyPI, Apache-2.0 / MIT; frequencies are uncopyrightable facts).
// Bins: core = Kernwortschatz (Zipf >= 4.5), common = häufig (>= 3.5),
// specialized = Fachsprache (>= 1.5). Items below 1.5 (incl. out-of-corpus
// compounds) carry NO bin on purpose — absence of corpus evidence is not
// "Fachsprache". Regenerate with \`pnpm build:frequency\` after content changes.
import type { Frequency } from "@/types";

export const frequencyGeneratedAt = "${today}";

export const frequency: Record<string, { bin: Frequency; zipf: number }> = {
${rows.join("\n")}
};

/** Frequency bin for a content id (vocab or collocation), if binnable. */
export const frequencyBin = (id: string): Frequency | undefined => frequency[id]?.bin;
`;
  await writeFile(OUT, file, "utf8");
  console.log(
    `✔ Wrote ${path.relative(root, OUT)}: ${binned}/${total} items binned ` +
      `(core ${stats.core} · common ${stats.common} · specialized ${stats.specialized} · unbinned ${stats.unbinned}).`,
  );
  const coverage = binned / total;
  if (coverage < 0.15) {
    console.warn(
      `⚠ Coverage ${(coverage * 100).toFixed(1)}% is below the 15% facet floor — the Häufigkeit facet will stay hidden.`,
    );
  }
}

main().catch((err) => {
  console.error("build-frequency failed:", err?.message ?? err);
  process.exitCode = 1;
});
